  /* USER CODE BEGIN 2 */
  printf("SPI scan start:");
  //CS_HIGH(); 	//Ideal State
  	HAL_Delay(10);

  	uint8_t tx = 0x8F; //RW=1, address=0x0F
  	uint8_t rx = 0;

  	CS_LOW();
  	HAL_SPI_Transmit(&hspi1, &tx, 1, HAL_MAX_DELAY);
  	HAL_SPI_Receive(&hspi1, &rx, 1, HAL_MAX_DELAY);
  	CS_HIGH();

  	printf("WHO_AM_I: 0x%02X\r\n", rx);

  	void LIS3_WriteReg(uint8_t reg, uint8_t data)
  	{
  	    /* Force bit 7 to 0 using & 0x7F this sets RW=0 (write mode).
  	     * Even if reg is passed with bit 7 set by mistake, the mask
  	     * guarantees the chip always sees a write command. */
  	    uint8_t tx[2] = { reg & 0x7F, data };
  	    CS_LOW();
  	    HAL_SPI_Transmit(&hspi1, tx, 2, HAL_MAX_DELAY);
  	    CS_HIGH();
  	}

  	uint8_t LIS3_ReadReg(uint8_t reg)
  	{
  	    /* Force bit 7 to 1 using | 0x80 this sets RW=1 (read mode).
  	     * Guarantees the chip always sees a read command regardless
  	     * of what is passed in. */
  	    uint8_t tx = reg | 0x80;
  	    uint8_t rx = 0;
  	    CS_LOW();
  	    HAL_SPI_Transmit(&hspi1, &tx, 1, HAL_MAX_DELAY);
  	    HAL_SPI_Receive(&hspi1, &rx, 1, HAL_MAX_DELAY);
  	    CS_HIGH();
  	    return rx;
  	}

  	CS_HIGH(); // CS idles HIGH — keeps chip in SPI mode, not I2C
  	HAL_Delay(10);

  	/* Write 0x67 to CTRL_REG4 (0x20):
  	 * ODR3=0, ODR2=1, ODR1=1, ODR0=0 → 100 Hz output data rate
  	 * BDU=0 → continuous update
  	 * Zen=1, Yen=1, Xen=1 → all axes enabled
  	 * Binary: 0110 0111 = 0x67 */
  	LIS3_WriteReg(0x20, 0x67);
